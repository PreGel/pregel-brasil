		<h3>Quais são os ingredientes básicos no gelato?</h3>
		Gelato é feito com ingredientes sólidos e líquidos, estabilizantes, emulsificantes, sabores, ar e outros ingredientes.
		Por exemplo:

		<ul>
			<li>Ingredientes Líquidos: água, leite, etc.</li>
			<li>Ingredientes sólidos: açúcares, gordura e sólidos do leite não gordurosos.</li>
			<li>Estabilizante: goma guar, goma de alfarroba, etc.</li>
			<li>Emulsificantes: mono e di-glicerídeos</li>
			<li>Ingredientes de sabor: pastas, sabor em pó</li>
			<li>Ar</li>
			<li>Outros ingredientes: Sais minerais, produtos de cacau, frutos secos, recheios, etc.</li>
		</ul>

		<h3>O que é a Base? </h3>
		As Bases da PreGel são ingredientes essenciais e equilibrados em pó, que promovem uma melhor qualidade, sabor, textura e fácil a produção de gelato. A base é essencial para a criação de uma estrutura e textura aveludada que permite a cremosidade e suavidade desejável pela qual a PreGel se orgulha.

		<h3>O que é o estabilizante?</h3>
		Existem dois tipos de estabilizantes usados no gelato. Um deles é de origem animal conhecido por gelatina. Os outros estabilizantes são de origem vegetal, tais como carragenina, goma guar e goma de alfarroba. Os estabilizantes são utilizados em gelato para uma variedade de razões, uma vez que aumentam a viscosidade, melhoram a suavidade do produto e previnem a formação de cristais de gelo e de fusão do gelato.

		<h3>O que é Arabeschi®?</h3>
		Arabeschi® é um creme utilizado para cobertura e recheio de gelato, sorbetto, sorvete, frozen yogurt, produtos de confeitaria, sobremesas e bebidas geladas. Ele é usado como recheio e como complemento para incrementar a decoração de sobremesas. O Arabeschi® adiciona extra sabor e textura as sobremesas. A nossa linha Arabeschi® engloba uma variedade de sabores, incluindo chocolate ao leite e amargo, nozes e amêndoas e uma variedade de frutas selecionadas.

		<h3>Quais são os ingredientes básicos para fazer o gelato?</h3>
		Na explicação mais básica, gelato é feito por mistura de leite ou água, açúcar e outros ingredientes naturais (tais como emulsificantes e estabilizantes), que formam a base, em seguida, a nata ou sabores distintos são adicionados.

		<h3>Qual a diferença entre processo frio e quente?</h3>
		A resposta mais fácil para isso é que o processo quente requer o aquecimento de líquidos, enquanto o processo frio não. Para saber mais sobre esses processos e suas diferenças, participe de uma de nossas aulas de treinamento em Campinas - SP.

		<h3>Quanto de sabor em pasta eu preciso utilizar?</h3>
		A quantidade de sabor de pasta para um tamanho de uma cuba de 5 litros padrão pode ser encontrado no livro de receitas PreGel ou em receitas na nossa base de dados. O livro de receitas pode ser enviado a você por seu representante de vendas ou de serviços especiais. Para lotes especiais, entre em contato com o nosso Centro de Formação Profissional.

		<h3>Quanto tempo eu posso manter meu gelato em uma vitrine?</h3>
		Esta resposta varia de acordo com vários fatores durante a preparação, mas geralmente entre 3-5 dias. Isso se o gelato for armazenado em um freezer durante à noite. Você também pode alcançar uma vida útil mais longa, preparando o seu gelato com o Processo quente – pasteurização. A melhor maneira de servir o gelato é quando ele está o mais fresco possível. Sempre incentivamos nossos clientes a seguirem estas dicas sempre que possível.

		<h3>Qual é a temperatura de estocagem?</h3>
		A temperatura de estocagem no congelador deve ser em torno de -17,8 ˚ Celsius.

		<h3>Qual é a temperatura do gelato quando sai da produtora? </h3>
		Entre -7.8˚e -5˚ Celsius – nesse momento apenas 50-60 por cento da água está congelada.

		<h3>Como devo apresentar meu gelato? </h3>
		Normalmente em cubas de 5 litros. Retire ela do congelador de forma muito natural, sem pressionar ou empurrar muito. O resultado deve ser uma forma muito harmoniosa com muitas ondas.

		<h3>Como servir um gelato?</h3>
		Enquanto o sorvete geralmente utiliza-se colheres, o gelato é servido com espátulas, que permitem valorizar a qualidade artesanal.

		<h3>Você decora seu gelato?</h3>
		Certamente. O Gelato é considerado uma sobremesa artesanal que significa que a decoração é uma grande parte de todo o ambiente da experiência com o gelato. Os Arabeschi® e as coberturas da PreGel são ambos usados para decorar o produto acabado. Além disso, os moldes da PreGel podem ser usados para criar sorvete de flores, gelato de tartarugas e muito mais, levando a cuba de gelato de visualmente atraente para deslumbrante.

		<h3>Qual é o prazo de validade dos produtos da PreGel? </h3>
		Os produtos da PreGel têm uma vida útil de três anos, a partir da data de fabricação, mesmo depois de abertos. No entanto é importante lembrar-se de manter os produtos bem fechados e não expô-los à temperaturas extremas. Caso sejam expostos à essas situações, o tempo de vida e a qualidade do produto podem ser reduzidos. A data de validade é indicada na embalagem e está em forma européia, que é dia / mês / ano. Se você tiver perguntas sobre a vida útil do produto, entre em contato com nossa Equipe de Atendimento Especial.

		<h3>O que devo fazer quando um Sabor em pasta, Arabeschi® ou um Topping começa a separar o óleo da pasta?</h3>
		Misture como uma espátula. Misture ocasionalmente para evitar a separação. Agite bem os toppings.

		<h3>Qual é o valor nutricional para o meu produto? </h3>
		A PreGel só pode fornecer informação nutricional dos produtos inacabados que produz. Se você quiser avaliar o conteúdo nutricional do produto acabado que você fornecer, recomendamos uma parceria com um laboratório de testes local que pode fornecer com precisão as informações nutricionais sobre o produto.

